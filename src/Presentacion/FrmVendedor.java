    private void btnGuerdarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuerdarActionPerformed

        try {

            String nombre = this.txtNombres.getText();
            String apellido = this.txtApellidos.getText();
            String telefono = this.txtTelefono.getText();
            String dni = this.txtDni.getText();

            if (txtNombres.equals("")) {
                JOptionPane.showMessageDialog(null, "Ingrese un Vendedor", Module.titleMessage, JOptionPane.CANCEL_OPTION);
            } else {

                EVendedor objVen = new EVendedor();
                if (id == 0) { //Registramos un nuevo VENDEDOR
                    objVen.setNombres(nombre);
                    objVen.setApellidos(apellido);
                    objVen.setTelefono(telefono);
                    objVen.setDni(dni);

                    String message = clsCli.create(objVen);
                    if (message.equals("Registrado correctamente")) {
                        JOptionPane.showMessageDialog(null, message, Module.titleMessage, JOptionPane.INFORMATION_MESSAGE);
                        cleanForm();
                    } else {
                        JOptionPane.showMessageDialog(null, message, Module.titleMessage, JOptionPane.CANCEL_OPTION);
                    }
                } else {
                    objVen.setId(id);
                    objVen.setNombres(nombre);
                    objVen.setApellidos(apellido);
                    objVen.setTelefono(telefono);
                    objVen.setDni(dni);

                    String message = clsCli.update(objVen);
                    if (message.equals("Actualizado correctamente")) {
                        JOptionPane.showMessageDialog(null, message, Module.titleMessage, JOptionPane.INFORMATION_MESSAGE);
                        cleanForm();
                        this.dispose();
                    } else {
                        JOptionPane.showMessageDialog(null, message, Module.titleMessage, JOptionPane.CANCEL_OPTION);
                    }
                }
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.toString(), Module.titleMessage, JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnGuerdarActionPerformed

    private void cleanForm(){
        txtNombres.setText("");
        txtApellidos.setText("");
        txtDni.setText("");
        txtTelefono.setText("");
        formActive.getCaller().loadTable();
    }
    
