    private void tblVendedorMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblVendedorMouseClicked
        // TODO add your handling code here:
        if (evt.getClickCount() == 2) {
            int id = (int) tblVendedor.getValueAt(tblVendedor.getSelectedRow(), 0);
            String nombres = tblVendedor.getValueAt(tblVendedor.getSelectedRow(), 1).toString();
            String apellidos = tblVendedor.getValueAt(tblVendedor.getSelectedRow(), 2).toString();
            String dni = tblVendedor.getValueAt(tblVendedor.getSelectedRow(), 3).toString();
            String telefono = tblVendedor.getValueAt(tblVendedor.getSelectedRow(), 4).toString();

            EVendedor ven = Module.ven;
            ven.setId(id);
            ven.setNombres(nombres);
            ven.setApellidos(apellidos);
            ven.setDni(apellidos);
            ven.setTelefono(telefono);

            EForm formActive = Module.formActive;
            formActive.setCaller(this);

            FrmVendedor formVendedor = new FrmVendedor();
            formVendedor.setVisible(true);
            formVendedor.pack();
        }
    }//GEN-LAST:event_tblVendedorMouseClicked

