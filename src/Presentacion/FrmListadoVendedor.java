    private void txtSearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSearchKeyReleased
        // TODO add your handling code here:
        try {

            String txtNombre = this.txtSearch.getText().trim();
            if (txtNombre.equals("")) {
                loadTable();
                return;
            }

            for (int i = this.defaultTable.getRowCount() - 1; i >= 0; i--) {
                this.defaultTable.removeRow(i);
            }

            Vendedor clsCli = new Vendedor();
            ArrayList arrayList = clsCli.searchByName(txtNombre);

            for (int i = 0; i < arrayList.size(); i++) {
                EVendedor vendedor = (EVendedor) arrayList.get(i);
                this.defaultTable.addRow(new Object[]{});

                this.defaultTable.setValueAt(vendedor.getId(), i, 0);
                this.defaultTable.setValueAt(vendedor.getNombres(), i, 1);
                this.defaultTable.setValueAt(vendedor.getApellidos(), i, 2);
                this.defaultTable.setValueAt(vendedor.getDni(), i, 3);
                this.defaultTable.setValueAt(vendedor.getTelefono(), i, 4);
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.toString(), Module.titleMessage, JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_txtSearchKeyReleased

